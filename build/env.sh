tc_ssh() {
    base_ssh_opts="-o ConnectTimeout=20 -o ServerAliveInterval=10 -o ServerAliveCountMax=2 -o NumberOfPasswordPrompts=1"
    if [ -n "${TC_PASSWORD_FILE:-}" ] && [ -f "$TC_PASSWORD_FILE" ]; then
        SSHPASS=$(cat "$TC_PASSWORD_FILE")
        export SSHPASS
        if [ -n "$TC_SSH_PROXYCOMMAND" ]; then
            sshpass -e ssh $base_ssh_opts $TC_SSH_OPTS -o "ProxyCommand=$TC_SSH_PROXYCOMMAND" "$@"
        else
            sshpass -e ssh $base_ssh_opts $TC_SSH_OPTS "$@"
        fi
        return
    fi
    if [ -n "$TC_SSH_PROXYCOMMAND" ]; then
        ssh $base_ssh_opts $TC_SSH_OPTS -o "ProxyCommand=$TC_SSH_PROXYCOMMAND" "$@"
    else
        ssh $base_ssh_opts $TC_SSH_OPTS "$@"
    fi
}

tc_scp() {
    if [ -n "${TC_PASSWORD_FILE:-}" ] && [ -f "$TC_PASSWORD_FILE" ]; then
        SSHPASS=$(cat "$TC_PASSWORD_FILE")
        export SSHPASS
        if [ -n "$TC_SSH_PROXYCOMMAND" ]; then
            sshpass -e scp -O $TC_SSH_OPTS -o "ProxyCommand=$TC_SSH_PROXYCOMMAND" "$@"
        else
            sshpass -e scp -O $TC_SSH_OPTS "$@"
        fi
        return
    fi
    if [ -n "$TC_SSH_PROXYCOMMAND" ]; then
        scp -O $TC_SSH_OPTS -o "ProxyCommand=$TC_SSH_PROXYCOMMAND" "$@"
    else
        scp -O $TC_SSH_OPTS "$@"
    fi
}

select_tool_triple() {
    pattern="$TOOLS/bin/$BUILD_EXPECT_TOOL_TRIPLE_GLOB-*gcc"
    set -- $pattern
    if [ $# -eq 0 ] || [ ! -e "$1" ]; then
        echo "Unable to find cross compiler matching $pattern" >&2
        return 1
    fi
    if [ $# -ne 1 ]; then
        echo "Multiple cross compilers matched $pattern" >&2
        printf '%s\n' "$@" >&2
        return 1
    fi
    basename "$1" | sed 's/-gcc$//'
}

